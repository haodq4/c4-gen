import fs from 'fs-extra';
import path from 'path';

export async function generateMarkdown(documentation, outputPath) {
  await fs.ensureDir(outputPath);
  
  // Generate main README.md
  const mainReadme = generateMainReadme(documentation);
  await fs.writeFile(path.join(outputPath, 'README.md'), mainReadme);
  
  // Generate individual level documents
  if (documentation.levels.context) {
    const contextDoc = generateContextDoc(documentation.levels.context, documentation);
    await fs.writeFile(path.join(outputPath, '01-context-diagram.md'), contextDoc);
  }
  
  if (documentation.levels.container) {
    const containerDoc = generateContainerDoc(documentation.levels.container, documentation);
    await fs.writeFile(path.join(outputPath, '02-container-diagram.md'), containerDoc);
  }
  
  if (documentation.levels.component) {
    const componentDoc = generateComponentDoc(documentation.levels.component, documentation);
    await fs.writeFile(path.join(outputPath, '03-component-diagram.md'), componentDoc);
  }
  
  if (documentation.levels.code) {
    const codeDoc = generateCodeDoc(documentation.levels.code, documentation);
    await fs.writeFile(path.join(outputPath, '04-code-diagram.md'), codeDoc);
  }
  
  // Generate mermaid diagrams
  await generateMermaidDiagrams(documentation, outputPath);
}

