function generateComponentMermaid(component) {
  const components = component.components || [];
  let mermaid = components.map((c, i) => `Comp${i}[${c.name}\\n${c.type}]`).join('\n    ');
  
  // Add dependencies
  components.forEach((c, i) => {
    c.dependencies?.forEach(dep => {
      const targetIndex = components.findIndex(t => t.name === dep.component);
      if (targetIndex >= 0) {
        mermaid += `\n    Comp${i} -->|${dep.type}| Comp${targetIndex}`;
      }
    });
  });
  
  return mermaid;
}

