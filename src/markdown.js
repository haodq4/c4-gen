function generateContainerMermaid(container) {
  const containers = container.containers || [];
  const dataStores = container.dataStores || [];
  
  let mermaid = containers.map((c, i) => `C${i}[${c.name}]`).join('\n    ');
  mermaid += '\n    ' + dataStores.map((ds, i) => `DB${i}[(${ds.name})]`).join('\n    ');
  
  // Add interactions
  containers.forEach((c, i) => {
    c.interactions?.forEach(inter => {
      const targetIndex = containers.findIndex(t => t.name === inter.to);
      if (targetIndex >= 0) {
        mermaid += `\n    C${i} -->|${inter.protocol}| C${targetIndex}`;
      }
    });
  });
  
  return mermaid;
}

