function generateSequenceMermaid(seq) {
  if (!seq || !seq.steps || !Array.isArray(seq.steps)) {
    return 'sequenceDiagram\n    Note: No steps defined\n';
  }
  
  let mermaid = 'sequenceDiagram\n';
  seq.steps?.forEach(step => {
    if (!step || !step.from || !step.to || !step.action) return;
    mermaid += `    ${step.from}->>+${step.to}: ${step.action}\n`;
    if (step.responseType) {
      mermaid += `    ${step.to}-->>-${step.from}: ${step.responseType}\n`;
    }
  });
  return mermaid;
}

function generateClassMermaid(code) {
  const classes = code.classes || [];
  return classes.map(cls => {
    // Safe check for class name
    if (!cls || !cls.name) return '';
    
    const className = cls.name.replace(/[^a-zA-Z0-9]/g, '_');
    let mermaid = `    class ${className} {\n`;
    
    // Properties
    cls.properties?.forEach(prop => {
      if (!prop || !prop.name || !prop.type) return;
      const visibility = prop.visibility === 'private' ? '-' : prop.visibility === 'protected' ? '#' : '+';
      mermaid += `        ${visibility}${prop.type} ${prop.name}\n`;
    });
    
    // Methods
    cls.methods?.forEach(method => {
      if (!method || !method.name) return;
      const visibility = method.visibility === 'private' ? '-' : method.visibility === 'protected' ? '#' : '+';
      const params = method.parameters?.map(p => `${p.name}: ${p.type}`).join(', ') || '';
      mermaid += `        ${visibility}${method.name}(${params}) ${method.returnType || 'void'}\n`;
    });
    
    mermaid += '    }\n';
    
    // Relationships
    cls.relationships?.forEach(rel => {
      if (!rel || !rel.to || !rel.type) return;
      const target = rel.to.replace(/[^a-zA-Z0-9]/g, '_');
      if (rel.type === 'extends') {
        mermaid += `    ${target} <|-- ${className}\n`;
      } else if (rel.type === 'implements') {
        mermaid += `    ${target} <|.. ${className}\n`;
      } else if (rel.type === 'has-a' || rel.type === 'uses') {
        mermaid += `    ${className} --> ${target}\n`;
      }
    });
    
    return mermaid;
  }).filter(m => m).join('\n'); // Filter out empty strings
}

function generateDatabaseSchemaDoc(schema) {
  const tables = schema.tables || [];
  
  return `
### Database Tables

${tables.map((table, index) => `
#### ${index + 1}. ${table.name}

**Description:** ${table.description}

**Columns:**

| Column | Type | PK | FK | Nullable | Unique | Default |
|--------|------|----|----|----------|--------|---------|
${table.columns?.map(col => `| ${col.name} | ${col.type} | ${col.isPrimaryKey ? '✓' : ''} | ${col.isForeignKey ? '✓' : ''} | ${col.isNullable ? 'Yes' : 'No'} | ${col.isUnique ? '✓' : ''} | ${col.defaultValue || '-'} |`).join('\n') || ''}

${table.columns?.filter(col => col.references).length > 0 ? `
**Foreign Keys:**
${table.columns.filter(col => col.references).map(col => `
- \`${col.name}\` → \`${col.references.table}.${col.references.column}\`
  - On Delete: ${col.references.onDelete}
  - On Update: ${col.references.onUpdate}
`).join('\n')}
` : ''}

${table.indexes?.length > 0 ? `
**Indexes:**
${table.indexes.map(idx => `- ${idx.name} (${idx.columns.join(', ')}) ${idx.isUnique ? '[Unique]' : ''} [${idx.type}]`).join('\n')}
` : ''}

${table.relationships?.length > 0 ? `
**Relationships:**
${table.relationships.map(rel => `- ${rel.type} with \`${rel.with}\` via \`${rel.foreignKey}\` - ${rel.description}`).join('\n')}
` : ''}
`).join('\n---\n')}

### Entity Relationship Diagram

\`\`\`mermaid
${schema.erdDiagram || generateERDMermaid(schema)}
\`\`\`
`;
}

function generateERDMermaid(schema) {
  const tables = schema.tables || [];
  let erd = 'erDiagram\n';
  
  tables.forEach(table => {
    table.relationships?.forEach(rel => {
      const cardinality = rel.type === 'one-to-many' ? '||--o{' : 
                         rel.type === 'many-to-one' ? '}o--||' :
                         rel.type === 'many-to-many' ? '}o--o{' : '||--||';
      erd += `    ${table.name} ${cardinality} ${rel.with} : "${rel.description || 'relates'}"\n`;
    });
  });
  
  return erd;
}

